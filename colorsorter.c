#pragma config(Sensor, port2,  touchLED,       sensorVexIQ_LED)
#pragma config(Sensor, port3,  colorDetector,  sensorVexIQ_Color12Color)
#pragma config(Sensor, port4,  distanceMM,     sensorVexIQ_Distance)
#pragma config(Sensor, port8,  bumpSwitch,     sensorVexIQ_Touch)
#pragma config(Motor,  motor1,          leftMotor,     tmotorVexIQ, openLoop, driveLeft, encoder)
#pragma config(Motor,  motor6,          rightMotor,    tmotorVexIQ, openLoop, reversed, driveRight, encoder)
#pragma config(Motor,  motor10,         armMotor,      tmotorVexIQ, openLoop, encoder)
#pragma config(Motor,  motor11,         clawMotor,     tmotorVexIQ, openLoop, encoder)
//*!!Code automatically generated by 'ROBOTC' configuration wizard               !!*//

void moveForward()
{
	setMotor(motor1, 50);
	setMotor(motor6, 50);
}

void addToCart()
{
	setMotor(armMotor, 50);
	wait(3, seconds);
	setMotor(clawMotor, -50);
	wait(5, seconds);
	setMotor(armMotor, -50);
	wait(2, seconds);
}

void stopAndPick()
{
	stopAllMotors();
	wait(1, seconds);
	setMotor(clawMotor, 50);
	wait(2, seconds);
}

void dropIt()
{
	setMotor(clawMotor, -50);
}

TSimpleColors chooseOneColor()
{
	TSimpleColors colorBox[4] = {colorRed, colorGreen, colorYellow, colorBlue};

	int i=0;
	TSimpleColors choosenColor;
	choosenColor = colorRed;
	
	for (i=0; i<40; i++)
	{
		setTouchLEDColor(touchLED, colorBox[i%4]);
		wait(5, seconds);
		
		if(getTouchLEDValue(touchLED) == true)
		{
			choosenColor = colorBox[i];
			break;
		}
	}
	return choosenColor;
}

task main()
{
	TSimpleColors myColor=colorNone;
	myColor = chooseOneColor();

	repeat (forever) 
	{
		if (getDistanceValue(distanceMM) < 70) 
		{
			stopAndPick();
			
			if (getColorName(colorDetector) == myColor) 
			{
				addToCart();
			} 
			else 
			{
				dropIt();
			}
		} 
		else 
		{
			moveForward();
			
			if(getTouchLEDValue(touchLED) == true)
			{
				myColor = chooseOneColor();
			}
		}
	}
}